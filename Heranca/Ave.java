package Heranca;

public class Ave extends Animal{
	
	private String tipoDePena;
	private String tipoDeBico;
	private boolean voa;
	
	
	public String getTipoDePena() {
		return tipoDePena;
	}
	public void setTipoDePena(String tipoDePena) {
		this.tipoDePena = tipoDePena;
	}
	public String getTipoDeBico() {
		return tipoDeBico;
	}
	public void setTipoDeBico(String tipoDeBico) {
		this.tipoDeBico = tipoDeBico;
	}
	public boolean isVoa() {
		return voa;
	}
	public void setVoa(boolean voa) {
		this.voa = voa;
	}
	

}
